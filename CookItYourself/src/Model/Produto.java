package Model;

public class Produto {

    private int id;
    private String nome;
    private float preco;
    private int estoque;

    public Produto() {
    }

    public Produto(int id, String nome, float preco) {
        this.id = id;
        this.nome = nome;
        this.preco = preco;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public float getPreco() {
        return preco;
    }

    public void setPreco(float preco) {
        this.preco = preco;
    }

    public int getEstoque() {
        return estoque;
    }

    public void setEstoque(int estoque) {
        this.estoque = estoque;
    }

    public int isEmEstoque() {
        return 1;
    }

    @Override
    public String toString() {
        return "" + getId() + " - Nome: " + getNome() + " Pre√ßo: R$" + getPreco() + " Estoque: " + getEstoque();
    }
}
